nome = input ('qual seu nome: ')
print('prazer em te conhecer {:20}'.format(nome)) # escreve em 20 espaços
print('prazer em te conhecer {:>20}'.format(nome)) #alinhado a direita em 20 espaços
print('prazer em te conhecer {:^20}'.format(nome)) #centralizado em 20 espaços
print('prazer em te conhecer {:=^20}'.format(nome))#vai ficar centralizado em 20 espaços com os iguais
